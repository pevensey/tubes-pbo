    private void auto_number(){
        System.out.println("kena1");
        try{
            String sql = "SELECT MAX(right(kode_user,2)) FROM penumpang";
            st = con.createStatement();
            rs = st.executeQuery(sql);
            System.out.println("kena2");
            while(rs.next()){
                System.out.println("kena3");
                if(rs.first() == false){
                    kode_user = ("U022");
                    System.out.println("kena4");
                }else{
                    System.out.println("kena5");
                    rs.last();
                    int auto_id = rs.getInt(1) +1;
                    String no = String.valueOf(auto_id);
                    int noLong=no.length();
                    for(int a=0;a<3-noLong;a++){
                        
                        no = "0" + no;
                    }
                    kode_user = ("U"+no);
                }
            }
        }catch(Exception e){
            System.out.println(e);
        }
    }
